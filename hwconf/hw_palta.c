// Defines
#define PALTA_FPGA_CLK_PORT			GPIOC
#define PALTA_FPGA_CLK_PIN			9

// Variables
static volatile bool i2c_running = false;

// I2C configuration
static const I2CConfig i2cfg = {
		OPMODE_I2C,
		100000,
		STD_DUTY_CYCLE
};

